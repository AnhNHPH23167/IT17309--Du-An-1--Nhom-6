    public void filldata(){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (SanPham viewModelSanPham : list) {
           dtm.addRow(new Object[]{
               viewModelSanPham.getMaSanPham(),viewModelSanPham.getTenSanPham()
           });
        }
    }

